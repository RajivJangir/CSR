#include<iostream>

int main(){
    
    int a = 15;  //Decimal
    int b = 017;  //Octal
    int c = 0x0f;  //Hexadecimal
    int d = 0b00001111;  //Binary

    std::cout<<"a = "<<a<<std::endl;
    std::cout<<"b = "<<b<<std::endl;
    std::cout<<"c = "<<c<<std::endl;
    std::cout<<"d = "<<d<<std::endl;
    
    return 0;
    
}